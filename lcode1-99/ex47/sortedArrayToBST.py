# Definition for a binary tree node.
class TreeNode(object):
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution(object):
    def sortedArrayToBST(self, nums):
        """
        :type nums: List[int]
        :rtype: TreeNode
        """
        if nums is None or len(nums) == 0:
            return None
        
        length = len(nums)
        rIdex = length//2
        root = TreeNode(nums[rIdex])
        root.left = self.sortedArrayToBST(nums[:rIdex])
        root.right = self.sortedArrayToBST(nums[rIdex+1:])
        return root

        


