class Solution(object):
    def isValidSudoku(self, board):
        """
        :type board: List[List[str]]
        :rtype: bool
        """
        hmap = {}
        rows = len(board)
        cols = len(board[0])
        
        for i in range(rows):
            for j in range(cols):
                num = board[i][j]
                if not num.isdigit():
                    continue
                else:
                    if num not in hmap.keys():
                        hmap[num] = [[i, j]]
                    else:
                        for item in hmap[num]:
                            x,  y = item
                            if x == i or y == j or (x/3 == i /3  and y /3 == j /3):
                                return False
                        hmap[num].append([i, j])
        return True
    
